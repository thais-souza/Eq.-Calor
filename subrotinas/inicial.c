//======================================================================================
// SUBROTINA PARA A SOLUÇÃO INICIAL DO PROBLEMA PRIMAL
//======================================================================================

void INICIALP ( double ***phi )
{
  //====================================================================================
  // DECLARAÇÃO DE VARIÁVEIS
  //====================================================================================
  int i , j ;        // VARIÁVEIS PARA O DESLOCAMENTO NAS DIREÇÕES X E Y
  double x , y ;     // VARIÁVEIS REAIS PARA O DESLOCAMENTO NAS DIREÇÕES X E Y
  //====================================================================================
  // CÁLCULO DA SOLUÇÃO INICIAL
  //====================================================================================
  for ( j = 0 ; j <= NY ; j ++ )
    {
      y = j * DY ;
      for ( i = 0 ; i <= NX ; i ++ )
	{
	  x = i * DX ;
	  phi [ i ] [ j ] [ 0 ] = PHIXYT0 ( x , y , 0.0 ) ;
	}
    }
}

